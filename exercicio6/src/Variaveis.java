public class Variaveis {
  private int a;
  private int b;
  private int falsoA;
  private int falsoB;

  public int getA() {
    return a;
  }

  public void setA(int a) {
    this.a = a;
  }

  public int getB() {
    return b;
  }

  public void setB(int b) {
    this.b = b;
  }

  public int getFalsoA() {
    return falsoA;
  }

  public void setFalsoA(int falsoA) {
    this.falsoA = falsoA;
  }

  public int getFalsoB() {
    return falsoB;
  }

  public void setFalsoB(int falsoB) {
    this.falsoB = falsoB;
  }
}
